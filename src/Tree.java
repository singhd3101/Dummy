
public class Tree {
	
	/**
	 * Applications - 
	 * 1) storing naturally hierarchical data, eg- file system
	 * 2) organize data for quick search, insertion, deletion eg - BST
	 * 3) Trie dictionary
	 * 4) Network routing algorithm
	 * 
	 * Binary Tree - for structuring ordered data
	 * 
	 * depth of a node - no of edges between root and that node
	 * 
	 * height of a tree - max no of edges from root to a leaf
	 * 
	 * complete binary tree - all levels except possibly the last are completely
	 * filled and all nodes are as left as possible
	 * 
	 * strict/proper binary tree - each node can have either 2 or 0 children
	 * 
	 * max no of nodes at level i - 2^i
	 * 
	 * root at level 0
	 * 
	 * perfect binary tree - all levels are completely filled
	 * 
	 * max number of nodes in a binary tree of height h - 2^h -1 
	 * 
	 * height of a tree with n nodes = log(n+1) - 1
	 * 
	 * balanced binary tree - difference between height of left and right subtree
	 * for every node is not more than k(mostly 1)
	 *
	 * sorted array -  removal and addition on O(n) and search in O(log n)
	 * 
	 * binary search tree - all operations in O(log n)
	 */

}
